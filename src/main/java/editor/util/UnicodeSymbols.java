package editor.util;

import java.text.Normalizer;

/**
 * This class is a collection of constants for various Unicode symbols.
 *
 * @author Alec Roelke
 */
public interface UnicodeSymbols
{
    /**
     * Latin small letter ae, '{@value #AE_LOWER}'.
     */
    char AE_LOWER = '\u00E6';
    /**
     * Bullet, '{@value #BULLET}'.
     */
    char BULLET = '\u2022';
    /**
     * Downwards black arrow, '{@value #DOWN_ARROW}'.
     */
    char DOWN_ARROW = '\u2B07';
    /**
     * Horizontal ellipsis, '{@value #ELLIPSIS}'.
     */
    char ELLIPSIS = '\u2026';
    /**
     * Em dash, '{@value #EM_DASH}'.
     */
    char EM_DASH = '\u2014';
    /**
     * Greater than or equal to, '{@value #GREATER_OR_EQUAL}'.
     */
    char GREATER_OR_EQUAL = '\u2265';
    /**
     * Infinity, '{@value #INFINITY}'.
     */
    char INFINITY = '\u221E';
    /**
     * Leftwards black arrow, '{@value #LEFT_ARROW}'.
     */
    char LEFT_ARROW = '\u2B05';
    /**
     * Less than or equal to, '{@value #LESS_OR_EQUAL}'.
     */
    char LESS_OR_EQUAL = '\u2264';
    /**
     * Minus sign, '{@value #MINUS}'.
     */
    char MINUS = '\u2212';
    /**
     * Multiplication sign, '{@value #MULTIPLY}'.
     */
    char MULTIPLY = '\u00D7';
    /**
     * Not equal to, '{@value #NOT_EQUAL}'.
     */
    char NOT_EQUAL = '\u2260';
    /**
     * Vulgar fraction one half, '{@value #ONE_HALF}'.
     */
    char ONE_HALF = '\u00BD';
    /**
     * Rightwards black arrow, '{@value #RIGHT_ARROW}'.
     */
    char RIGHT_ARROW = '\u2B95';
    /**
     * Superscript two, '{@value #SUPERSCRIPT_TWO}'.
     */
    char SUPERSCRIPT_TWO = '\u00B2';
    /**
     * Upwards black arrow, '{@value #UP_ARROW}'.
     */
    char UP_ARROW = '\u2B06';
    /**
     * Substitute character, '{@value #SUBSTITUTE}'.
     */
    char SUBSTITUTE = '\u001A';

    /**
     * Create a string with ASCII approximations of special characters, in lower
     * case.
     * 
     * @param str string to normalize
     * @return A lower-case string with all special characters replaced with ASCII
     * approximations, such as {@value #AE_LOWER} becomes 'ae.'
     */
    static String normalize(String str)
    {
        return Normalizer.normalize(str.toLowerCase(), Normalizer.Form.NFD).replaceAll("\\p{M}", "").replace(String.valueOf(AE_LOWER), "ae");
    }
}
